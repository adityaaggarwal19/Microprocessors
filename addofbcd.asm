;WAP to 32 bit BCD addition
.MODEL SMALL
.386
.DATA
CR DB 0
.CODE
.STARTUP
MOV EBX,0
MOV EDX,0
MOV CX,8
INP1:
    SHL EBX,4
    MOV AH,01
    INT 21H
    SUB AL,30H
    ADD BL,AL
    LOOP INP1 
MOV DL,"+"
MOV AH,02
INT 21H
MOV CX,8
INP2:
    SHL EDX,4
    MOV AH,01
    INT 21H
    SUB AL,30H
    ADD DL,AL
    LOOP INP2
MOV CX,4
CLC
ADDITION:
    MOV AL,DL
    ADD AL,CR
    ADC AL,BL
    DAA 
    JNC L1
        MOV CR,1
    JMP L2
    L1:MOV CR,0
    L2:
    MOV BL,AL
    SHR EDX,8
    ROR EBX,8
    LOOP ADDITION
    MOV DL,"="
MOV AH,02
INT 21H
MOV ECX,8
MOV EDX,0
DIS:
    MOV EDX,EBX
    AND EDX,0F0000000H
    ROL EDX,4
    ADD DL,30H
    MOV AH,02
    INT 21H
    SHL EBX,4
    LOOP DIS
.EXIT
END